package br.unama.fabsoft.Modelo;

public class Paciente {
	private int numeroProntuario;
	private String nome;
	private char sexo;
	private String dataNascimento;
	private String estadoCivil;
	private String telefone;
	private String endereco;
	private String municipio;
	private String naturalidade;
	private String etniaRegiliao;
	private String email;
	
	private String rendaFamiliar;
	private String composicaoFamiliar;
	private String grauInstrucao;
	private String profissao;
	private String horarioTrabalho;
	public int getNumeroProntuario() {
		return numeroProntuario;
	}
	public void setNumeroProntuario(int numeroProntuario) {
		this.numeroProntuario = numeroProntuario;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public char getSexo() {
		return sexo;
	}
	public void setSexo(char sexo) {
		this.sexo = sexo;
	}
	public String getDataNascimento() {
		return dataNascimento;
	}
	public void setDataNascimento(String dataNascimento) {
		this.dataNascimento = dataNascimento;
	}
	public String getEstadoCivil() {
		return estadoCivil;
	}
	public void setEstadoCivil(String estadoCivil) {
		this.estadoCivil = estadoCivil;
	}
	public String getTelefone() {
		return telefone;
	}
	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}
	public String getEndereco() {
		return endereco;
	}
	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}
	public String getMunicipio() {
		return municipio;
	}
	public void setMunicipio(String municipio) {
		this.municipio = municipio;
	}
	public String getNaturalidade() {
		return naturalidade;
	}
	public void setNaturalidade(String naturalidade) {
		this.naturalidade = naturalidade;
	}
	public String getEtniaRegiliao() {
		return etniaRegiliao;
	}
	public void setEtniaRegiliao(String etniaRegiliao) {
		this.etniaRegiliao = etniaRegiliao;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getRendaFamiliar() {
		return rendaFamiliar;
	}
	public void setRendaFamiliar(String rendaFamiliar) {
		this.rendaFamiliar = rendaFamiliar;
	}
	public String getComposicaoFamiliar() {
		return composicaoFamiliar;
	}
	public void setComposicaoFamiliar(String composicaoFamiliar) {
		this.composicaoFamiliar = composicaoFamiliar;
	}
	public String getGrauInstrucao() {
		return grauInstrucao;
	}
	public void setGrauInstrucao(String grauInstrucao) {
		this.grauInstrucao = grauInstrucao;
	}
	public String getProfissao() {
		return profissao;
	}
	public void setProfissao(String profissao) {
		this.profissao = profissao;
	}
	public String getHorarioTrabalho() {
		return horarioTrabalho;
	}
	public void setHorarioTrabalho(String horarioTrabalho) {
		this.horarioTrabalho = horarioTrabalho;
	}
	
	
}
